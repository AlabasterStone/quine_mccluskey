n = 4
# 一个n位数，每一位为0或1，用程序枚举所有的组合情况
for i in range(2**n):
    s = bin(i)[2:]  # 转化为二进制字符串
    s = '0' * (n - len(s)) + s  # 前面补0
    #每个字符中间加空格
    s = ' '.join(s)
    #如果字符串中1的个数大于等于3，字符串后加空格再加"1"
    if s.count('1') >= 3:
        print(f'm{i} | {s} | 1')
    else:
        print(f'm{i} | {s} | 0')

